package example;

import java.util.Scanner;

public class Solution {

	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		
		int t = scan.nextInt();
		for (int test_case = 1; test_case <= t; ++test_case) {
			
			
			
			System.out.println("#" + test_case + " ");
		}
		scan.close();
	}
}
